generate_waybar_config() {
    local tpl="$DOTFILES_DIR/waybar/.config/waybar/config.jsonc.tpl"
    local out="$DOTFILES_DIR/waybar/.config/waybar/config.jsonc"
    local content
    # Skip the first line (template banner comment) when reading
    content="$(tail -n +2 "$tpl")"
    # Substitute each placeholder with the resolved semantic hex for the current mode
    for name in STRONG FG WARNING ERROR OK; do
        content="${content//@@${name}@@/#$(color "$name")}"
    done
    {
        echo "// E-ink waybar config ($MODE mode) -- AUTO-GENERATED by theme/generate.sh"
        printf '%s\n' "$content"
    } > "$out"
}

